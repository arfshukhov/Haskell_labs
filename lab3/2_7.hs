-- Определение функции at для доступа к элементу списка по индексу
at :: [a] -> Int -> a
(x:_)  `at` 0 = x                     
(_:xs) `at` n | n > 0 = xs `at` (n - 1) 
_      `at` _ = error "Индекс вне границ списка" 

lst :: [Integer]
lst = [1, 2, 3, 4]

