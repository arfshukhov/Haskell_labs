--задача 2.6 

{-
    6. Напишите функцию rotate, реализующую циклическую ротацию  за-
 данного списка влево, т.е.
 
 
    > rotate 3 [1..10]
    [4,5,6,7,8,9,10,1,2,3]
 
 
    Напишите также бесточечную реализацию rotate?
-}

f a lst = drop a lst ++ take a lst

f' n = (++) <$> (drop n) <*> (take n) 

test = f 3 [1..10] == [4,5,6,7,8,9,10,1,2,3] &&
       f 2 [2,6,1,5,8] == [1,5,8,2,6] &&
       f' 3 [1..10] == [4,5,6,7,8,9,10,1,2,3] &&
       f' 2 [2,6,1,5,8] == [1,5,8,2,6]